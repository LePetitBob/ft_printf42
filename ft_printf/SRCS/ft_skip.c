#include "../INCLUDES/ft_printf.h"

int	ft_skip(char *text, int i)
{
	while ((text[i] < 65 || (text[i] > 90 && text[i] < 97) || text[i] > 122)
		&& text[i] != '%' && text[i])
		i++;
	return (i);
}
